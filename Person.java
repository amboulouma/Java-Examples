public class Person {
    
    //attributes
    private int age;
    private String name;
    private String code;

    //constructors
    //Default constructor
    public Person () {
        this.age = 0;
        this.name = "";
        this.code = "";
    }

    //Constructor with parameters
    public Person (int age, String name, String code) {
        this.age = age;
        this.name = name;
        this.code = code;
    }



    public static void main(String [ ] args) {
        System.out.println("Lol");
    }

}

